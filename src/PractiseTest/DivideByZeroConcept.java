package PractiseTest;

public class DivideByZeroConcept {

    public static void main(String[] args){

        //Divide by Integer, Double and Float
        //System.out.println(10/0); //Arithmetic Exception
        System.out.println(10.0/0); //Decimal Value
        System.out.println(10/0.0); //Decimal zero
        System.out.println(10.34d/0); //Divide Double value
        System.out.println(10.34f/0); //Divide Float value
        //System.out.println(0/0); //Arithmetic Exception
        System.out.println(0.0/0);
        System.out.println(0/0.0);
        System.out.println(0.0/0.0);
    }
}
